#include <stdio.h>  // Standard input-output library

int main() {
    int a = 10;  // Declaring variable a

    // Unary increment: Increments the value of a by 1
    printf("Value of a before increment: %d\n", a);
    a++;  // Increments a by 1
    printf("Value of a after increment: %d\n", a);

    // Unary decrement: Decrements the value of a by 1
    printf("Value of a before decrement: %d\n", a);
    a--;  // Decrements a by 1
    printf("Value of a after decrement: %d\n", a);

    return 0;  // Return 0 to indicate successful execution
}
